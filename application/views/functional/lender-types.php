                    <p>CMBS loans are always non-recourse as well since the collateralization is primarily based on the property which can be a huge draw for many borrowers. However, CMBS loans can be somewhat complex at times. Loan structures can become incredibly involved with multiple conditions and restrictions not found with other loan types. Rates are not locked until right before closing, which can make a stark difference for larger loan amounts if the transaction occurs during a volatile market. Required legal fees for CMBS financing can be cumbersome and excessive compared to other loan types.</p>
